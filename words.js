let freqPos = {};
for ( let letter of lettersEn )
	freqPos[letter] =  new Array(5).fill(0);

// how mauch any letter of english alphabet using in any position of word
// for example
// a: [ 141, 304, 307, 163, 64 ]
// b: [ 173, 16, 57, 24, 11 ]
for ( let word in words )
	for ( let i=0; i<5; i++ )
		freqPos[word[i]][i]++;

// weight of every word = frequency of repetition of a letter in the first position + 
// frequency of repetition in the second position + ... fifth position
// for example 
// artsy: 1054 = a(141) + r(267) + t(111) + s(171) + y(364)
for ( let word in words ) {
	words[word] = 0;
	for ( let i=0; i<5; i++ ) {
		words[word] += freqPos[word[i]][i];
	}
}

